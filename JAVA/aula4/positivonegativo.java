//Escreva um programa para determinar se o número é positivo ou negativo

package aula4;

public class positivonegativo {

    public static void main(String[] args) {
        
        int num;
        num = -1;

        if(num < 0){
            System.out.println("num negativo");
        }else if (num > 0){
            System.out.println("num positivo");
        }else{
            System.out.println("num é zero");
        }



    }
    
}
